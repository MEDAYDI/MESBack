    public String   ResetPasswordEmail(String username){

        User user =  userRepo.findUserByUsername(username);
        if (user == null){
            throw new UsernameNotFoundException("User not found !!");
        }

        String token = UUID.randomUUID().toString();
        ResetPasswordToken resetPasswordToken =new ResetPasswordToken(
                token ,
                LocalDateTime.now(),
                LocalDateTime.now().plusMinutes(15),
                user
        );
        restPasswordTokenService.saveConfirmationToken(resetPasswordToken);

        String link = "http://localhost:56230//resetPassword"  ;


        try {
            emailSender.send(user.getEmail(),
                    buildEmail(user.getName(),link));
        } catch (MessagingException e) {
            e.printStackTrace();
        }

        return token ;

    }


    public void ResetPassword( String token ,  String password ){
        ResetPasswordToken resetPasswordToken =     restPasswordTokenService
                .getToken(token)
                .orElseThrow(() -> new IllegalStateException("token not fond"));
        if (resetPasswordToken.getConfirmedAt()!=null){
            throw new IllegalStateException("email already confirmed");
        }
        restPasswordTokenService.setConfirmedAt(token);
        String username =resetPasswordToken.getUser().getUsername();
        String encodedPassword = bCryptPasswordEncoder
                .encode(password);
        userRepo.restPassword(encodedPassword,username);

    }
